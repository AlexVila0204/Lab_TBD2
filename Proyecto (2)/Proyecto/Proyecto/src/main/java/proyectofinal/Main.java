    private void AgregarintegranteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarintegranteActionPerformed
//Crear Alumnos
        ArrayList<Object> atributos = new ArrayList<>();
        DefaultTableModel tableModel = (DefaultTableModel) CrearTablaAlumnos.getModel();
        for (int i = 0; i < tableModel.getColumnCount(); i++) {
            Object valorCelda = tableModel.getValueAt(0, i);
            atributos.add((valorCelda != null) ? valorCelda.toString() : "null");

        }
        Persona p = new Persona(atributos.get(1).toString(), "0000", 0000, 0000, atributos.get(1).toString(), "", "", "NO");
        ListUssers.add(p);
        guardarEnArchivo();

        for (int i = 0; i < tableModel.getColumnCount(); i++) {
            tableModel.setValueAt("", 0, i);
        }

        Item item = new Item()
                .withPrimaryKey("cedula", atributos.get(0).toString())
                .withString("nombre", atributos.get(1).toString())
                .withString("apellido_1", atributos.get(2).toString())
                .withString("apellido_2", atributos.get(3).toString())
                .withString("tipo_lic", atributos.get(4).toString());
        com.mycompany.proyecto.Proyecto.crearItem("Alumno", item);

        CrearTablaAlumnos.setModel(tableModel);

    }//GEN-LAST:event_AgregarintegranteActionPerformed


    public DefaultTableModel TraerTabla(String nombreTabla) {
        Table table = dynamoDB.getTable(nombreTabla);
        ArrayList<String> Atributos = new ArrayList<String>();
        switch (nombreTabla) {

            case "Matriculas": {
                ArrayList<String> NombresColumnas = new ArrayList<String>();
                NombresColumnas.add("n_matricula");
                NombresColumnas.add("dni_alumno");
                NombresColumnas.add("tipo_lic");
                NombresColumnas.add("fecha_matricula");

                DefaultTableModel tableModel = new DefaultTableModel(NombresColumnas.toArray(), 0);
                ItemCollection<ScanOutcome> items = table.scan();
                Iterator<Item> iter = items.iterator();
                while (iter.hasNext()) {
                    Item item = iter.next();
                    Object[] row = new Object[]{item.get("n_matricula"), item.get("dni_alumno"), item.get("tipo_lic"), item.get("fecha_matricula")};
                    tableModel.addRow(row);
                }
                return tableModel;
            }

            case "Examenes": {
                //continuar con los eamenes
                ArrayList<String> NombresColumnas = new ArrayList<String>();
                NombresColumnas.add("fecha_examen");
                NombresColumnas.add("tipo_examen");
                NombresColumnas.add("matricula");
                NombresColumnas.add("n_examen");
                NombresColumnas.add("informe");
                NombresColumnas.add("resultado");

                DefaultTableModel tableModel = new DefaultTableModel(NombresColumnas.toArray(), 0);
                ItemCollection<ScanOutcome> items = table.scan();
                Iterator<Item> iter = items.iterator();
                while (iter.hasNext()) {
                    Item item = iter.next();
                    Object[] row = new Object[]{item.get("fecha_examen"), item.get("tipo_examen"), item.get("matricula"), item.get("n_examen"), item.get("informe"), item.get("resultado")};
                    tableModel.addRow(row);
                }
                return tableModel;
            }

        }
        return null;
    }


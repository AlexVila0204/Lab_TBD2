    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        String entidad = (String) CB_Crear.getSelectedItem();
        switch (entidad) {
            case "Matriculas": {
                Table tabla = dynamoDB.getTable("Matriculas");
                if (tabla.getItem("dni_alumno", Uactual.getID()) == null) {
                    ArrayList<String> NombresColumnas = new ArrayList<String>();
                    NombresColumnas.add("n_matricula");
                    NombresColumnas.add("dni_alumno");
                    NombresColumnas.add("tipo_lic");
                    NombresColumnas.add("fecha_matricula");
                    DefaultTableModel tableModel = new DefaultTableModel(NombresColumnas.toArray(), 0);
                    Object[] row = new Object[]{"", Uactual.getID(), "", ""};
                    tableModel.addRow(row);

                    CrearTablaMatriculas.setModel(tableModel);
                    CrearMatriculas.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Solo 1 matricula por alumno, intentelo mas tarde");
                }
            }
            break;
            case "Examenes": {
                Table tabla = dynamoDB.getTable("Examenes");
                Table tabla2 = dynamoDB.getTable("Matriculas");

                Item it = tabla2.getItem("dni_alumno", Uactual.getID());
                Item Br = tabla.getItem("matricula", it.get("n_matricula"));

                if (Br == null) {
                    int nota = 0;

                    // Pregunta 1
                    String respuesta1 = JOptionPane.showInputDialog(null, "¿Se puede pasar un semáforo en rojo?");
                    if (respuesta1 != null && respuesta1.equalsIgnoreCase("No")) {
                        nota++;
                    }

                    // Pregunta 2
                    Object[] opciones = {"Verdadero", "Falso"};
                    int respuesta2 = JOptionPane.showOptionDialog(null,
                            "¿Se puede usar el teléfono al conducir?", "Pregunta",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                            opciones, opciones[0]);
                    if (respuesta2 == JOptionPane.YES_OPTION) {
                        nota++;
                    }

                    // Crear un nuevo ítem actualizado con la nota
                    Item itemActualizado = new Item()
                            .withPrimaryKey("fecha_examen", Br.getString("fecha_examen"))
                            .withString("tipo_examen", Br.getString("tipo_examen"))
                            .withString("matricula", Br.getString("matricula"))
                            .withInt("n_examen", Br.getInt("n_examen"))
                            .withString("informe", Br.getString("informe"))
                            .withBoolean("resultado", nota >= 1); // Actualizar el resultado basado en la nota

                    // Borrar el ítem existente
                    tabla.deleteItem("matricula", it.get("n_matricula"));

                    // Insertar el nuevo ítem actualizado
                    tabla.putItem(itemActualizado);

                    // Mostrar la nota final
                    JOptionPane.showMessageDialog(null, "Tu nota es: " + nota + "/2");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Solo 1 Examen por alumno, inténtelo más tarde");
                }

            }
            break;
            default:
                // Si el valor del String no coincide con ninguna de las opciones anteriores
                break;
        }

    }


